# concept of indexing
c= "hello"


# types of indexing
# positive indexing

print(c[4])


# negative indexing
print(c[-1])
print(c[-2])


# slicing

d= 'hello world'
print(d[0:5]) #in this last number is not included
print(d[2:])
print(d[:8])
print(d[2:6:2]) #the third one works as increment

print(d[::-1])#syntax to reverse a string